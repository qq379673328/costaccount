package cn.com.fanrenlee.auth.dic;

/**
 * 单条字典项
 */
public class DicItem {

	/**
	 * 字典值
	 */
	private String code;

	/**
	 * 字典描述
	 */
	private String desc;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

}
